CREATE TABLE Variant (
Source_DB_ID VARCHAR(255) NULL,
Gene_Name VARCHAR(255) NULL,
Variant_AA_Change VARCHAR(255) NULL,
Cytogenetic_Band VARCHAR(255) NULL,
Chromosome_Coordinate VARCHAR(255) NULL,
Ensembl_gene_ID VARCHAR(255) NULL,
Ensembl_transcript_ID VARCHAR(255) NULL,
Ensembl_translation_ID VARCHAR(255) NULL
);
